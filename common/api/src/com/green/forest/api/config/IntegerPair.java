package com.green.forest.api.config;

public class IntegerPair {
	
	public int a;
	public int b;

	public IntegerPair(int a, int b) {
		super();
		this.a = a;
		this.b = b;
	}
	
	
	public IntegerPair() {
		super();
	}



	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	
	
	
	

}
